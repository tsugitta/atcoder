void solve() {
  ll N;
  cin >> N;

  string Vacant = "Vacant";
  string Male = "Male";
  string Female = "Female";

  ll left_i = 0;
  cout << left_i << endl;

  string left;
  cin >> left;
  if (left == Vacant) return;

  ll right_i = N - 1;
  cout << right_i << endl;

  string right;
  cin >> right;
  if (right == Vacant) return;

  while (true) {
    ll mid_i = (left_i + right_i) / 2;
    cout << mid_i << endl;

    string mid;
    cin >> mid;
    if (mid == Vacant) return;

    if ((mid_i - left_i) % 2 == 0 && left != mid ||
        (mid_i - left_i) % 2 == 1 && left == mid) {
      right_i = mid_i;
      right = mid;
    } else {
      left_i = mid_i;
      left = mid;
    }
  }
}

