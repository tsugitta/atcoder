void solve() {
  ll N, C;
  cin >> N >> C;

  ll ADD = 0;
  ll REMOVE = 1;

  struct Event {
    ll day, val, type;
  };

  V<Event> events;

  rep(i, N) {
    ll a, b, c;
    cin >> a >> b >> c;
    events.push_back({a, c, ADD});
    events.push_back({b + 1, c, REMOVE});
  }

  sort(all(events), [](Event lhs, Event rhs) { return lhs.day < rhs.day; });

  ll last_price = 0;
  ll last_day = 0;
  ll res = 0;

  for (auto e : events) {
    res += (e.day - last_day) * min(C, last_price);

    last_day = e.day;
    if (e.type == ADD) last_price += e.val;
    if (e.type == REMOVE) last_price -= e.val;
  }

  cout << res << "\n";
}
