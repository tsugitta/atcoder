void solve() {
  ll N, M;
  cin >> N >> M;

  VPL ps(M);
  for (auto& [a, b] : ps) cin >> a >> b;

  sort(all(ps), [&](PL lhs, PL rhs) {
    if (lhs.fi != rhs.fi) return lhs.fi < rhs.fi;
    // a が等しい線分は一つしか選べない。b の昇順で並び替えると LIS
    // でどちらも選択できてしまう
    // b の降順で並び替えて一つしか選べないようにする
    return lhs.se > rhs.se;
  });

  ll res = 0;

  VL lis(N, INF);
  for (auto& [_, b] : ps) {
    auto it = lower_bound(all(lis), b);
    ll i = it - lis.begin();
    lis[i] = b;
    chmax(res, i + 1);
  }

  cout << res << "\n";
}
