const ll INF = 1e18;

void solve() {
  ll N, K;
  ll R, S, P;
  string T;
  cin >> N >> K;
  cin >> R >> S >> P;
  cin >> T;

  map<char, ll> pt_map;
  pt_map['r'] = R;
  pt_map['s'] = S;
  pt_map['p'] = P;

  map<char, char> win_map;
  win_map['r'] = 'p';
  win_map['s'] = 'r';
  win_map['p'] = 's';

  ll res = 0;
  V<char> hands(N);

  rep(i, N) {
    char next = T[i];

    char want = win_map[next];

    if (i >= K && hands[i - K] == want) {
      hands[i] = '#';
      continue;
    }

    hands[i] = want;
    res += pt_map[want];
  }

  cout << res << endl;
}

